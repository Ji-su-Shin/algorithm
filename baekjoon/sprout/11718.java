import java.io.*;
import java.util.*;

public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        String userInput;
        while((userInput = br.readLine()) != null) {
            System.out.println(userInput);
        }
        br.close();
        return;
    }
}

